CREATE TABLE PROBABILIDAD_EVENTO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EVENTO VARCHAR2(100),
    DESCRIPCION VARCHAR2(200),
    VALOR_A NUMBER,
    VALOR_B NUMBER,
    TOTAL_CASOS NUMBER,
    CASOS_FAVORABLES NUMBER,
    PROBABILIDAD NUMBER,
    FECHA_CALCULO DATE,
    USUARIO_CREACION VARCHAR2(50),
    ESTADO VARCHAR2(20),
    METODO VARCHAR2(50),
    VARIABLE_1 NUMBER,
    VARIABLE_2 NUMBER,
    VARIABLE_3 NUMBER,
    VARIABLE_4 NUMBER,
    VARIABLE_5 NUMBER,
    RESULTADO_TEXTO VARCHAR2(200),
    OBSERVACIONES VARCHAR2(200)
);

